numero = int(input("Digite O 1 numero:"))

numero2 = int(input("Digite O 2 numero:"))

tipodeconta = str(input("Tipo de Calculo:"))

if tipodeconta == "+":
    print(numero+numero2)

if tipodeconta == "-":
    print(numero-numero2)


if tipodeconta == "*":
    print(numero*numero2)

if tipodeconta == "/":
    print(numero/numero2)

if tipodeconta =="**":
    print(numero**numero2)

if tipodeconta =="//":
    print(numero//numero2)



